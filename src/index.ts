/**
 * Suma dos numeros
 * @param firstnumber - El primer operando de la suma
 * @param secondnumber - El segundo operando de la suma
 * @returns El resultado de la suma
 * ```typescript
 * add(1, 7) = 8
 * add(8, -3) = 5
 * ```
 */

export function add(firstnumber: number, secondnumber: number) : number {
    return firstnumber + secondnumber;
}